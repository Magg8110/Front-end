//for (inicializacion, condicion, incremento/decremento)

var x = 0;
for (x; x < 5; x++) {     //x++ suma
    console.log("El valor de x es :"+x);
}

//se repite hasta que no cumpla la condicion (ejemplo <5)

var x = 10;
for (x; x > 0; x--) {     //x-- resta
    console.log("El valor de x es :"+x);
}


//se usa para cuando sabes cuantas iteraciones va a tener el programa